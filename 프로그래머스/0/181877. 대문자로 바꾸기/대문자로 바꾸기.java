/**
* @brief 소문자를 대문자로 변환
* @details 소문자는 대문자로 대문자일 경우에는 대문자 그대로 변환
* @param String myString
* @return String answer
*/

class Solution {
    public String solution(String myString) {
        String answer = "";
        String str = myString.toUpperCase();
        
        answer = str;
        
        return answer;
    }
}